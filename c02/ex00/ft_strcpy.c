char	*ft_strcpy(char *dest, char *src)
{
	int		i;
	char	*p_dest;

	i = 0;
	p_dest = dest;
	while (1)
	{
		dest[i] = src[i];
		if (src[i] == '\0')
			return (p_dest);
		i++;
	}
}
